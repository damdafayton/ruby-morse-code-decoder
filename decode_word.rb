@morse_dict = { 'a' => '.-', 'b' => '-...', 'c' => '-.-.', 'd' => '-..', 'e' => '.',
                'g' => '--.', 'k' => '-.-', 'l' => '.-..', 'm' => '--', 'f' => '..-.',
                'n' => '-.', 'o' => '---', 'w' => '.--', '2' => '..---', '3' => '...--',
                'x' => '-..-', 'p' => '.--.', 'q' => '--.-', 'r' => '.-.', 's' => '...',
                't' => '-', 'u' => '..-', 'v' => '...-', 'i' => '..', '4' => '....-', 'j' => '.---',
                'y' => '-.--', 'z' => '--..', ' ' => ' ', '1' => '.----',
                '5' => '.....', '6' => '-....', '7' => '--...', '8' => '---..',
                '9' => '----.', '0' => '-----', 'h' => '....' }

def decode_word(let)
  reverse_dict = {}

  @morse_dict.each do |key, value|
    reverse_dict[value] = key
  end

  letters_list = let.split
  word = ''

  letters_list.each do |letter|
    word += reverse_dict[letter]
  end
  word
end
